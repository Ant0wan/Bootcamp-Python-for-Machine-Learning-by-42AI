if __name__ == '__main__':
    if len(sys.argv) > 1:
        try:
            j, s, enc = 0, [], []
            for i in sys.argv[1::]:
                s.append(i.upper())
            for word in s:
                enc.append('')
                for char in word:
                    enc[j] += mapping[char] + ' '
                j += 1
            print(' / '.join(enc), end='\n')
        except KeyError:
            print('ERROR', end='\n')
